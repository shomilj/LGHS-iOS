    func messaging(_ messaging: Messaging, didReceiveRegistrationToken fcmToken: String) {
        print("Firebase registration token: \(fcmToken)")
        let dataDict:[String: String] = ["token": fcmToken]
        NotificationCenter.default.post(name: Notification.Name("FCMToken"), object: nil, userInfo: dataDict)
        // TODO: If necessary send token to application server.
        // Note: This callback is fired at each app startup and whenever a new token is generated.
        
        DispatchQueue.global(qos: .background).async {
            if let type = UserUtil.userType, let uuid = Auth.auth().currentUser?.uid {
                if type == .parent {
                    Database.database().reference().child(Keys.Database.version)
                        .child(Keys.Database.fcmTokens)
                        .child("parents")
                        .child(uuid)
                        .setValue(fcmToken)
                } else if type == .teacher {
                    Database.database().reference().child(Keys.Database.version)
                        .child(Keys.Database.fcmTokens)
                        .child("staff")
                        .child(uuid)
                        .setValue(fcmToken)
                } else if type == .student {
                    if let studentId = UserUtil.getCurrentStudent()?.id {
                        Database.database().reference().child(Keys.Database.version)
                            .child(Keys.Database.fcmTokens)
                            .child("students")
                            .child(uuid)
                            .setValue(["id": studentId, "token": fcmToken])
                    }
                }
            }
        }
    }
    
